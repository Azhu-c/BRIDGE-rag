def try_run_ll_file(filename, ir_test=None, ir_func=None, temp_dir: str = "/tmp"):
    os.makedirs(temp_dir, exist_ok=True)
    #print(filename)
    ll_object_file = filename.replace('.ll', '.o')
    compile_success = False
    exec_success = False
    similarity = -1.0

    if ir_func:
        with open(filename, 'r') as f:
            gen_code = f.read()
        similarity = get_normalized_edit_similarity(ir_func, gen_code)
    try:
        result_compile = subprocess.run(
            ['clang', '-c', filename, '-o', ll_object_file, '-lm', '-lc'],
            capture_output=True, text=True, timeout=15
        )

        if result_compile.returncode != 0:
            #print(result_compile.stderr)
            return False, False, result_compile.stderr, similarity
        compile_success = True
        
        if ir_test:
            with tempfile.NamedTemporaryFile(delete=False, suffix=".c", dir=temp_dir) as c_temp_file:
                c_temp_file_path = c_temp_file.name
                c_temp_file.write(ir_test.encode())

            c_object_file = c_temp_file_path.replace('.c', '.o')
            result_c = subprocess.run(
                ['clang', '-c', c_temp_file_path, '-o', c_object_file, '-lm', '-lc'],
                capture_output=True, text=True, timeout=15
            )

            if result_c.returncode != 0:
                #print(result_c.stderr)
                if os.path.exists(ll_object_file): os.remove(ll_object_file)
                if os.path.exists(c_temp_file_path): os.remove(c_temp_file_path)
                return True, False, result_c.stderr, similarity

            output_binary = filename.replace('.ll', '_exe')
            result_link = subprocess.run(
                ['clang', ll_object_file, c_object_file, '-lm', '-lc', '-o', output_binary],
                capture_output=True, text=True, timeout=15
            )

            if result_link.returncode != 0:
                #print(result_link.stderr)
                if os.path.exists(c_temp_file_path): os.remove(c_temp_file_path)
                if os.path.exists(c_object_file): os.remove(c_object_file)
                if os.path.exists(ll_object_file): os.remove(ll_object_file)
                return True, False, result_link.stderr, similarity

            result_exec = subprocess.run(
                [output_binary],
                capture_output=True, text=True, timeout=15
            )

            if result_exec.returncode != 0:
                #print(result_exec.stderr)
                if os.path.exists(output_binary): os.remove(output_binary)
                if os.path.exists(c_temp_file_path): os.remove(c_temp_file_path)
                if os.path.exists(c_object_file): os.remove(c_object_file)
                if os.path.exists(ll_object_file): os.remove(ll_object_file)
                return True, False, result_exec.stderr, similarity

            exec_success = True
            if os.path.exists(output_binary): os.remove(output_binary)
            if os.path.exists(c_temp_file_path): os.remove(c_temp_file_path)
            if os.path.exists(c_object_file): os.remove(c_object_file)
            if os.path.exists(c_object_file): os.remove(c_object_file)

        #else:
            #success = True

        #if ir_func:
            #with open(filename, 'r') as f:
                #gen_code = f.read()
                #similarity = get_normalized_edit_similarity(ir_func, gen_code)

    except subprocess.TimeoutExpired:
        return False, False, "TimeoutExpired", similarity
    finally:
        if os.path.exists(ll_object_file):
            os.remove(ll_object_file)

    return compile_success, exec_success, "", similarity


def run_refinement(
    json_path: str,
    input_ll_dir: str,
    output_ll_dir: str,
    file_pattern: str = "onlyfunc_{task_id}_{opt}.o.ll",
    retry_limit: int = 5,
    api_key: str | None = None,
    base_url: str = "https://api.deepseek.com",
    llm_model_name: str = "deepseek-chat",
    llm_timeout: int = 300,
    temp_dir: str = "/tmp",
):
    with open(json_path, 'r') as f:
        testsets = json.load(f)

    os.makedirs(output_ll_dir, exist_ok=True)
    os.makedirs(temp_dir, exist_ok=True)

    edit_sim_dict = defaultdict(list)
    edit_sim_all = []
    success_counter_compile = defaultdict(int)
    success_counter_exec = defaultdict(int)

    client = create_openai_client(api_key=api_key or os.environ.get("DEEPSEEK_API_KEY", ""), base_url=base_url)

    for testset in testsets:
        task_id = testset["task_id"]
        opt = testset["type"]
        ir_test = testset["ir_test"]
        input_asm = testset["input_asm_prompt"]
        ir_func = testset["ir_func"]

        candidate_path = testset.get("output_ll_path")
        if candidate_path:
            filename = candidate_path
        else:
            filename = os.path.join(input_ll_dir, file_pattern.format(task_id=task_id, opt=opt))

        print(filename)
        if not os.path.exists(filename):
            print(f"File not found: {filename}")
            continue

        compile_success, exec_success, error_msg_compile, similarity = try_run_ll_file(filename, ir_test, ir_func, temp_dir=temp_dir)

        retry_count = 1
        current_file = filename
        while not exec_success and retry_count <= retry_limit:
            print(f"-------------retry----------{retry_count}---------------------------{current_file}")
            with open(current_file, 'r') as f:
                func0_code = f.read()

            errors_msg = error_msg_compile
            errors_msg1 = "."

            if "Assertion" in error_msg_compile:
                fixed_code = fix_with_llm_api_testcases(func0_code, errors_msg, errors_msg1, client, input_asm, ir_test, model_name=llm_model_name, timeout=llm_timeout)
            elif "PHI" in error_msg_compile:
                fixed_code = fix_with_llm_api_phi(func0_code, errors_msg, errors_msg1, client, input_asm, model_name=llm_model_name, timeout=llm_timeout)
            else:
                fixed_code = fix_with_llm_api(func0_code, errors_msg, errors_msg1, client, input_asm, model_name=llm_model_name, timeout=llm_timeout)

            retry_dir = os.path.join(output_ll_dir, f"retry_{retry_count}")
            os.makedirs(retry_dir, exist_ok=True)
            file_feedback = os.path.join(retry_dir, os.path.basename(filename))
            with open(file_feedback, "w") as f:
                f.write(fixed_code)
            current_file = file_feedback

            compile_success, exec_success, error_msg_compile, similarity = try_run_ll_file(current_file, ir_test, ir_func, temp_dir=temp_dir)
            retry_count += 1

        if compile_success:
            success_counter_compile[opt] += 1
        if exec_success:
            success_counter_exec[opt] += 1
        if similarity >= 0:
            edit_sim_dict[opt].append(similarity)
            edit_sim_all.append(similarity)

    print("\n=== Compile & Executable Summary ===")
    for opt in ["O0", "O1", "O2", "O3"]:
        compile_count = success_counter_compile[opt]
        exec_count = success_counter_exec[opt]
        sims = edit_sim_dict[opt]
        avg_sim = sum(sims) / len(sims) if sims else 0.0
        print(f"{opt}: Compile Success = {compile_count}, Executable Success = {exec_count}, Avg Similarity = {avg_sim:.4f}")

    total_compile = sum(success_counter_compile.values())
    total_exec = sum(success_counter_exec.values())
    avg_all = sum(edit_sim_all) / len(edit_sim_all) if edit_sim_all else 0.0
    print(f"\nTotal: Compile Success = {total_compile}, Executable Success = {total_exec}, Avg Similarity = {avg_all:.4f}")