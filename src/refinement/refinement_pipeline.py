_MAIN_PATTERN = re.compile(r'int\s+main\s*\([^)]*\)\s*{', re.MULTILINE)
def extract_main_body(text: str) -> str:
    """
    : C （ \\n ，）
    : main （），
    """
    code = text

    if "\\n" in code and "\n" not in code:
        code = code.replace("\\n", "\n")

    m = _MAIN_PATTERN.search(code)
    if not m:
        return ""

    start_brace = code.find("{", m.start())
    if start_brace == -1:
        return ""

    depth = 0
    end_brace = None
    for i, ch in enumerate(code[start_brace:], start_brace):
        if ch == "{":
            depth += 1
        elif ch == "}":
            depth -= 1
            if depth == 0:
                end_brace = i
                break

    if end_brace is None:
        return ""

    body = code[start_brace + 1:end_brace]
    return body.strip()


